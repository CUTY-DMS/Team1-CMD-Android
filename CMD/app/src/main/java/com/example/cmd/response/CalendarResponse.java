package com.example.cmd.response;

public class CalendarResponse {

    private String title;
    private Long year;
    private Long month;
    private Long day;

    public String getTitle() {
        return title;
    }

    public Long getYear() {
        return year;
    }

    public Long getMonth() {
        return month;
    }

    public Long getDay() {
        return day;
    }
}
